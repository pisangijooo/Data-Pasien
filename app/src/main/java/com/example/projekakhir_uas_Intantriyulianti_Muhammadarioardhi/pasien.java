package com.example.projekakhir_uas_Intantriyulianti_Muhammadarioardhi;

public class pasien {

    public String id, nama, email, alamat, jeniskelamin, goldarah, riwayatpenyakit, gejala, tanggal;

    public pasien(String id, String nama, String email, String alamat, String jeniskelamin,
                  String goldarah, String riwayatpenyakit, String gejala, String tanggal) {
        this.id = id;
        this.nama = nama;
        this.email = email;
        this.alamat = alamat;
        this.jeniskelamin = jeniskelamin;
        this.goldarah = goldarah;
        this.riwayatpenyakit = riwayatpenyakit;
        this.gejala = gejala;
        this.tanggal = tanggal;
    }
}
